FONT_FAMILY = "Segoe UI"
HEADER_FONT = (FONT_FAMILY, 22, "bold")
SECTION_FONT = (FONT_FAMILY, 14, "bold")
BODY_FONT = (FONT_FAMILY, 12)
SMALL_FONT = (FONT_FAMILY, 11)

# ──────────────────────── Core Logic ───────────────────────────

def analyze_with_llama(report_text):
    """Safely calls Ollama with error handling for CUDA/Connection issues."""
    try:
        prompt = f"""
        Analyze this blood report. Provide:
        1. A bulleted list of OUT-OF-RANGE values only.
            - format should be: "Value Name: Actual Value (Normal Range) - High or Low"
        2. A medium-length health summary.
        3. A brief recommendation.
        4. A lifestyle advice.

        Rules:
        1. Address patient as Mr. or Ms. based on the name in the report.
        2. Use simple language, avoid medical jargon.
        3. Focus on actionable insights, not just numbers.
        4. If all values are normal, say "All values are within the normal range."
        5. Provide general health advice if no issues are found.
        6. Do not mention the AI or the analysis process in the output (just the header).
        7. Do not add a header saying Blood Analysis Report anymore, just start with the content directly.

        Report:
        {report_text}
        """

        response = ollama.chat(
            model='llama3:8b',
            messages=[{'role': 'user', 'content': prompt}],
            options={
                'temperature': 0.2,
                'num_predict': 2000 
            }
        )
        return response['message']['content']
    except Exception as e:
        return f"## ❌ AI Error\nThe LLM process failed. This is usually due to a CUDA/GPU memory error or Ollama not being running.\n\nDetails: {str(e)}"


def ocr_convert(image_path):
    """Run OCR on an image. Forces CPU to save VRAM for the AI model."""
    # We initialize inside the function to ensure resources are local to the thread
    converter = DocumentConverter() 
    result = converter.convert(image_path)
    markdown_content = result.document.export_to_markdown()

    warnings = []
    for element, _level in result.document.iterate_items():
        if hasattr(element, 'prov') and element.prov:
            for p in element.prov:
                conf = getattr(p, 'confidence', None)
                if conf is None and hasattr(p, 'extra') and p.extra:
                    conf = p.extra.get("confidence")
                if conf is not None and conf < 0.80:
                    text_val = getattr(element, 'text', "[Table/Group]")
                    if text_val.strip():
                        warnings.append(f"Low Confidence ({conf:.2f}): '{text_val.strip()}'")
    
    # Cleanup OCR objects immediately
    del converter
    gc.collect() 
    
    return markdown_content, warnings


def send_email(sender, app_password, to_email, subject, body):
    gmail.username = sender
    gmail.password = app_password
    gmail.send(
        subject=subject,
        receivers=[to_email],
        text=body,
    )

